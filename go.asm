mesg2 db "PLAYER2 WIN!"
drawresult db "    draw!   "
wordsize dw 12  
.code
gameover proc far 
    mov ax,@data
    mov ds,ax 
    ; change graphics mode
    mov ah,0
    mov al,13h
    int 10h 
	writewinner p1cl,p2cl,p1lives,p2lives,mesg1,mesg2,drawresult ,wordsize    
    mov color , 10h  
	gl: 
     cmp frame ,0 
     je g0 
     cmp frame, 1 
     je g1
     cmp frame, 2 
     je g2
     cmp frame, 3 
     je g3
     cmp frame, 4 
     je g4
     cmp frame, 5 
     je g5
     cmp frame, 6 
     je g6
     cmp frame ,7 
     je g7
     g0:lea SI , go1
     jmp then  
     g1:lea SI , go2
     jmp then
     g2:lea SI , go3
     jmp then
     g3:lea SI , go4
     jmp then
     g4:lea SI , go5
     jmp then  
     g5:lea SI , go6
     jmp then
     g6:lea SI , go7
     jmp then 
     g7:lea SI , go8
     then:mov cx , gx
     mov dx , gy 
     mov mgx , cx  
     mov mgy , dx
     add mgx , 66
     add mgy , 30   
     outlop:
         mov cx , gx
         inlop:
         mov al,[SI]
		 and al , al
		 jz dbp
		 mov al , color
         dbp:mov ah,0ch 
         int 10h   
         inc cx 
         inc SI
         cmp cx,mgx
         jl inlop 
     inc dx  
     cmp dx , mgy
     jl outlop 
	 xor frameincrease,1
	 mov ax ,frameincrease
     add frame,ax 
	 mov cx ,0FFFFh
     cmp frame , 8
     jl skip
	 inc color 
     mov frame , 0
	skip:NOP
;print options
	mov ah,1
    int 16h 
    jz nopress
    cmp ah,048h
    jz up
    cmp ah,050h
    jz down
	cmp ah,01ch
	jz quit
    jmp press
    up:
    dec currentoption
    jns press
	mov ax,optionssize
	dec ax
    mov currentoption,ax
    jmp press
    down:
    inc currentoption
    jmp press
    press:
    mov ah,0ch
    mov al,0
    int 21h
	;mod to wrap
    mov ax,currentoption
    mov dx,optionssize
    div dl
    mov al,0            
    xchg al,ah
    mov currentoption,ax
    nopress:
	;init for int 10h
    mov dh, 18;row
	mov bh,0 ;Page 0
    mov cx,optionssize;loop to print option
    mov bp,offset option1
    mov si,offset option1;to change cx because [bp]==ss:bp
    push ds;to make es equal to ds
    pop es;int 10h es:bp 
    printoptions:
    push cx
    mov bl,8h
    mov ax,optionssize
    sub ax, currentoption
    cmp ax,cx
	jnz white	
	mov bl,0fh
	white:
    mov al, 1
    mov cx, [si]
    mov ch,0
    inc si
    add si,cx
    inc bp
	;set cursor to middel of screen
    mov dl,40
	sub dl,cl
	shr dl,1
	mov ah, 13h
    int 10h
	;increas bp for the upcoming message
	add bp,cx
	dec dl
	mov ah, 2
	int 10h
	inc dh;new line/row
	pop cx
    loop printoptions
	call Delay
	jmp gl
quit: 
	ret 
gameover endp 
